#!/bin/bash

# -----------------------------------------------------------------------------------------

logger Backups starting...

# -----------------------------------------------------------------------------------------

BACKUP_DIR=/opt
LOG_FILE=/var/log/backups.log

# -----------------------------------------------------------------------------------------

rm -rf ${LOG_FILE}
mkdir -p "${BACKUP_DIR}"

# -----------------------------------------------------------------------------------------

echo
echo "Start Date:                 `date`"

# -----------------------------------------------------------------------------------------

echo "    cloud-ap:/opt/backups:  `date`"
rsync -av cloud-ap:/opt/backups ${BACKUP_DIR}/ --log-file=${LOG_FILE} $* &> /dev/null

# -----------------------------------------------------------------------------------------

echo "End Date:                   `date`"

logger Backups complete...

# -----------------------------------------------------------------------------------------

