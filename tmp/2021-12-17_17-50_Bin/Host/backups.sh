#/bin/bash

# -----------------------------------------------------------------------------------------

logger Backups starting...

# -----------------------------------------------------------------------------------------

LOG_FILE=/var/log/backups.log

# -----------------------------------------------------------------------------------------

rm -rf ${LOG_FILE}

# -----------------------------------------------------------------------------------------

echo
echo "Start Date:           `date +'%F %a'`"

# -----------------------------------------------------------------------------------------

BACKUP_DIR=/mnt/desktop-ap/exports/backups/host/`hostname`/
mkdir -p ${BACKUP_DIR}

echo "    desktop-ap:"
echo "        /etc:                        `date +'%H:%M:%S'`"
rsync -av $1 /etc ${BACKUP_DIR} --log-file=${LOG_FILE} $* &> /dev/null

# -----------------------------------------------------------------------------------------

echo "End Date:             `date +'%F %a %H:%M:%S'`"

logger Backups complete...

# -----------------------------------------------------------------------------------------